$('#crmeduc_curso_interesse').on('change', function () {
   // Define o valor como variável
   var selectValue = $(this).val();
   // Limpa o campo e adiciona o primeiro item
   $('#crmeduc_campus_interesse').empty();
   $('#crmeduc_campus_interesse').append("<option value='' disabled selected>Selecione o polo</option>");
   // items dentro de cada select
   for (i = 0; i < poloEad[selectValue].length; i++) {
      // retorno dos itens
      $('#crmeduc_campus_interesse').append("<option value='" + poloEad[selectValue][i] + "'>" + poloEad[selectValue][i] + "</option>");
   }
});

